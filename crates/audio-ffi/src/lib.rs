    // -------------------------------------------------------------------
    // Filesystem scan commands (Library page)
    // -------------------------------------------------------------------
    const SCAN_MAX_DEPTH: u32 = 8;
    const SCAN_MAX_ENTRIES: usize = 50_000;

    #[tauri::command]
    pub fn scan_directory(
        app_handle: tauri::AppHandle,
        path: String,
    ) -> Result<Vec<FileEntry>, String> {
        let root = PathBuf::from(&path);
        if !root.is_dir() {
            return Err(format!("not a directory: {}", path));
        }
        let art_dir = ensure_art_cache_dir(&app_handle);
        read_dir_entries(&root, &art_dir).map_err(|e| e.to_string())
    }

    #[tauri::command]
    pub async fn scan_library(
        app_handle: tauri::AppHandle,
        roots: Vec<String>,
        extensions: Vec<String>,
    ) -> Result<Vec<FileEntry>, String> {
        let art_dir = ensure_art_cache_dir(&app_handle);
        let handle = app_handle.clone();
        tauri::async_runtime::spawn_blocking(move || {
            scan_library_inner(roots, extensions, &art_dir, Some(&handle))
        })
        .await
        .map_err(|e| e.to_string())?
    }

    /// Shared scanning logic used by both the `scan_library` command and
    /// `LibraryDb::rescan_library`. Takes an explicit art cache directory
    /// instead of resolving it from an `AppHandle`.
    ///
    /// When `app_handle` is `Some`, progress events are emitted as
    /// `"scan-progress"` Tauri events during the walk so the frontend can
    /// show a live progress indicator.
    pub(crate) fn scan_library_inner(
        roots: Vec<String>,
        extensions: Vec<String>,
        art_dir: &PathBuf,
        app_handle: Option<&tauri::AppHandle>,
    ) -> Result<Vec<FileEntry>, String> {
        let ext_set: HashSet<String> = extensions
            .into_iter()
            .map(|e| {
                let t = e.trim().to_lowercase();
                if t.starts_with('.') { t } else { format!(".{}", t) }
            })
            .collect();

        let mut entries = Vec::new();
        let mut visited = HashSet::new();

        for root_str in &roots {
            let root = PathBuf::from(root_str);
            let canon = root.canonicalize().unwrap_or_else(|_| root.clone());
            if !canon.is_dir() { continue; }
            let base_offset = entries.len();
            walk_dir(&canon, 0, &ext_set, &mut visited, &mut entries, base_offset, art_dir, app_handle);
            if entries.len() >= SCAN_MAX_ENTRIES { break; }
        }

        sort_entries(&mut entries);
        let total = entries.len().min(SCAN_MAX_ENTRIES);
        entries.truncate(SCAN_MAX_ENTRIES);

        if let Some(handle) = app_handle {
            let _ = handle.emit("scan-progress", serde_json::json!({
                "phase": "complete",
                "current": total,
                "total": total,
            }));
        }

        Ok(entries)
    }

    fn ensure_art_cache_dir(app_handle: &tauri::AppHandle) -> PathBuf {
        let data_dir = app_handle.path().app_data_dir().unwrap_or_else(|_| PathBuf::from("/tmp"));
        let art_dir = data_dir.join("utoaudio").join("art");
        let _ = std::fs::create_dir_all(&art_dir);
        art_dir
    }

    fn read_dir_entries(dir: &Path, art_dir: &PathBuf) -> std::io::Result<Vec<FileEntry>> {
        let mut out = Vec::new();
        for entry in fs::read_dir(dir)? {
            let entry = match entry { Ok(e) => e, Err(_) => continue };
            let is_dir = entry.file_type().map(|ft| ft.is_dir()).unwrap_or(false);
            if let Some(fe) = build_file_entry(&entry.path(), is_dir, art_dir) {
                out.push(fe);
            }
        }
        sort_entries(&mut out);
        Ok(out)
    }

    fn walk_dir(
        dir: &Path, depth: u32,
        ext_set: &HashSet<String>,
        visited: &mut HashSet<PathBuf>,
        out: &mut Vec<FileEntry>,
        base_offset: usize,
        art_dir: &PathBuf,
        app_handle: Option<&tauri::AppHandle>,
    ) {
        if depth >= SCAN_MAX_DEPTH { return; }
        let canon = dir.canonicalize().unwrap_or_else(|_| dir.to_path_buf());
        if !visited.insert(canon.clone()) { return; }

        let iter = match fs::read_dir(dir) {
            Ok(it) => it,
            Err(_) => { visited.remove(&canon); return; }
        };

        for entry in iter.flatten() {
            let path = entry.path();
            let is_dir = path.is_dir();
            if path.file_name()
                .and_then(|n| n.to_str())
                .map(|n| n.starts_with('.'))
                .unwrap_or(false)
            { continue; }

            if !is_dir {
                let ext = path.extension()
                    .and_then(|e| e.to_str())
                    .map(|e| format!(".{}", e.to_lowercase()));
                match ext {
                    Some(e) if ext_set.contains(&e) => {}
                    _ => continue,
                }
            }

            if let Some(fe) = build_file_entry(&path, is_dir, art_dir) {
                out.push(fe);
            }
            if let Some(handle) = app_handle {
                if out.len() % 100 == 0 {
                    let root_label = dir.to_string_lossy().into_owned();
                    let _ = handle.emit("scan-progress", serde_json::json!({
                        "phase": "scanning",
                        "current": out.len() + base_offset,
                        "root": root_label,
                    }));
                }
            }
            if out.len() >= SCAN_MAX_ENTRIES { break; }
            if is_dir {
                walk_dir(&path, depth + 1, ext_set, visited, out, base_offset, art_dir, app_handle);
                if out.len() >= SCAN_MAX_ENTRIES { break; }
            }
        }
        visited.remove(&canon);
    }

    fn build_file_entry(path: &Path, is_directory: bool, art_dir: &PathBuf) -> Option<FileEntry> {
        let name = path.file_name()?.to_str()?.to_string();
        let abs = path.canonicalize().unwrap_or_else(|_| path.to_path_buf());
        let abs_str = abs.to_string_lossy().into_owned();
        let (size, modified) = if is_directory {
            (None, None)
        } else {
            let meta = fs::metadata(path).ok();
            let size = meta.as_ref().map(|m| m.len());
            let modified = meta.and_then(|m| {
                m.modified().ok()
                    .and_then(|t| t.duration_since(UNIX_EPOCH).ok())
                    .map(|d| d.as_secs() as i64)
            });
            (size, modified)
        };
        let (album_art_path, title, artist, album, sample_rate, bits_per_sample) = if is_directory {
            (None, None, None, None, None, None)
        } else {
            let art = crate::library::discover_album_art(&abs_str, art_dir);
            let (title, artist, album, sr, bps) = read_audio_meta(&abs_str);
            (art, title, artist, album, sr, bps)
        };
        Some(FileEntry {
            name,
            path: abs_str,
            is_directory,
            size,
            modified,
            album_art_path,
            title,
            artist,
            album,
            sample_rate,
            bits_per_sample,
        })
    }

    /// Read the track title, artist, album, sample rate, and bit depth from
    /// an audio file's tags + properties in a single lofty pass.
    fn read_audio_meta(path: &str) -> (Option<String>, Option<String>, Option<String>, Option<u32>, Option<u32>) {
        let parse_options = ParseOptions::new().read_properties(true);
        let probe = match Probe::open(path) {
            Ok(p) => p.options(parse_options),
            Err(_) => return (None, None, None, None, None),
        };
        let probe = match probe.guess_file_type() {
            Ok(p) => p,
            Err(_) => return (None, None, None, None, None),
        };
        let tagged_file = match probe.read() {
            Ok(f) => f,
            Err(_) => return (None, None, None, None, None),
        };
        let tag = tagged_file.primary_tag().or_else(|| tagged_file.first_tag());
        let title = tag.and_then(|t| t.get_string(&ItemKey::TrackTitle).map(|s| s.to_string()));
        let artist = tag.and_then(|t| t.get_string(&ItemKey::TrackArtist).map(|s| s.to_string()));
        let album = tag.and_then(|t| t.get_string(&ItemKey::AlbumTitle).map(|s| s.to_string()));
        let props = tagged_file.properties();
        let sample_rate = props.sample_rate();
        let bits_per_sample = props.bit_depth().map(|b| b as u32);
        (title, artist, album, sample_rate, bits_per_sample)
    }

    fn sort_entries(entries: &mut [FileEntry]) {
        entries.sort_by(|a, b| {
            b.is_directory.cmp(&a.is_directory)
                .then_with(|| a.name.to_lowercase().cmp(&b.name.to_lowercase()))
        });
    }

