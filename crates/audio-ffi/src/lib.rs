    #[cfg(target_os = "linux")]
    #[tauri::command]
    pub fn set_output_device(
        engine: tauri::State<'_, Arc<AudioEngine>>,
        backend: String,
        alsa_device: Option<String>,
    ) -> Result<(), String> {
        let mut current: AppSettings = settings_load().unwrap_or_default();
        current.output_device = Some(crate::settings::OutputDeviceSettings {
            backend: backend.clone(),
            alsa_device: alsa_device.clone(),
        });
        settings_save(&current)?;

        if backend == "alsa" {
            audio_core::set_linux_alsa_device(alsa_device);
        } else {
            // Switching back to PipeWire: release the ALSA device reservation
            // so PipeWire can reclaim the DAC, then clear the preference.
            #[cfg(target_os = "linux")]
            audio_core::release_reserved_alsa_device();
            audio_core::set_linux_alsa_device(None);
        }

        // Force engine rebuild on next prepare() so the new device takes effect.
        engine.shutdown().map_err(|e| e.to_string())?;
        engine.init();
        engine.prepare(None).map_err(|e| e.to_string())?;

        Ok(())
    }

