    #[cfg(test)]
    mod tests {
        use super::*;
        use std::fs;
        use std::path::PathBuf;
        use std::sync::atomic::{AtomicU64, Ordering};

        static COUNTER: AtomicU64 = AtomicU64::new(0);

        fn unique_tmp_dir(label: &str) -> PathBuf {
            let n = COUNTER.fetch_add(1, Ordering::Relaxed);
            let pid = std::process::id();
            let dir = std::env::temp_dir()
                .join(format!("utoaudio-audio-ffi-test-{label}-{pid}-{n}"));
            fs::create_dir_all(&dir).expect("create tmp dir");
            dir
        }

        #[test]
        fn read_dir_entries_reports_is_directory_correctly() {
            let dir = unique_tmp_dir("read-dir-entries");
            let sub = dir.join("subdir");
            fs::create_dir(&sub).expect("create subdir");
            let file = dir.join("song.flac");
            fs::write(&file, b"hello").expect("write file");

            let art_dir = std::env::temp_dir().join("utoaudio-test-art");
            let _ = std::fs::create_dir_all(&art_dir);
            let entries = read_dir_entries(&dir, &art_dir).expect("read_dir_entries ok");
            assert_eq!(entries.len(), 2, "expected exactly two entries");

            let by_name: std::collections::HashMap<&str, &FileEntry> = entries
                .iter()
                .map(|e| (e.name.as_str(), e))
                .collect();

            let dir_entry = by_name
                .get("subdir")
                .expect("subdir entry present");
            assert!(
                dir_entry.is_directory,
                "subdir must have is_directory=true (was {})",
                dir_entry.is_directory
            );
            assert!(
                dir_entry.size.is_none(),
                "subdir must have size=None (was {:?})",
                dir_entry.size
            );

            let file_entry = by_name
                .get("song.flac")
                .expect("song.flac entry present");
            assert!(
                !file_entry.is_directory,
                "song.flac must have is_directory=false (was {})",
                file_entry.is_directory
            );
            assert_eq!(
                file_entry.size,
                Some(5),
                "song.flac must report its real size"
            );

            fs::remove_dir_all(&dir).ok();
        }
    }
