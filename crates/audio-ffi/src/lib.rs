    #[tauri::command]
    pub fn play(engine: tauri::State<'_, Arc<AudioEngine>>, song: SongInfo) -> Result<(), String> {
        eprintln!("[utoaudio] play: path={} sr={:?} bps={:?}", song.path, song.sample_rate, song.bits_per_sample);
        let preferred_sr = song.sample_rate;
        audio_core::set_preferred_bits_per_sample(song.bits_per_sample);
        if !engine.is_initialized() {
            engine.init();
            // Try the requested rate, then 48 kHz (common USB default),
            // then None. The first successful prepare "primes" the ALSA
            // device so subsequent re-prepares at other rates work.
            engine.prepare(preferred_sr)
                .or_else(|_| engine.prepare(Some(48_000)))
                .or_else(|_| engine.prepare(None))
                .map_err(|e| e.to_string())?;
        } else if preferred_sr.is_some() {
            engine.prepare(preferred_sr)
                .or_else(|_| engine.prepare(Some(48_000)))
                .or_else(|_| engine.prepare(None))
                .map_err(|e| e.to_string())?;
        }
        let play_result = engine.play(song.clone());
        eprintln!("[utoaudio] engine.play result: {:?}", play_result.is_ok());
        play_result.map_err(|e| e.to_string())?;

        // Cache the song info for NowPlaying retrieval and persist as last-played.
        if let Ok(mut cache) = SONG_INFO_CACHE.lock() {
            let map = cache.get_or_insert_with(HashMap::new);
            map.insert(song.path.clone(), song.clone());
        }
        if let Ok(mut cur) = CURRENT_SONG_INFO.lock() {
            *cur = Some(song.clone());
        }
        persist_last_played(&song);
        Ok(())
    }

    #[tauri::command]
    pub fn queue_next(engine: tauri::State<'_, Arc<AudioEngine>>, song: SongInfo) -> Result<(), String> {
        let preferred_sr = song.sample_rate;
        audio_core::set_preferred_bits_per_sample(song.bits_per_sample);
        if !engine.is_initialized() {
            engine.init();
            engine.prepare(preferred_sr)
                .or_else(|_| engine.prepare(Some(48_000)))
                .or_else(|_| engine.prepare(None))
                .map_err(|e| e.to_string())?;
        } else if preferred_sr.is_some() {
            engine.prepare(preferred_sr)
                .or_else(|_| engine.prepare(Some(48_000)))
                .or_else(|_| engine.prepare(None))
                .map_err(|e| e.to_string())?;
        }
        engine.queue_next(song.clone()).map_err(|e| e.to_string())?;

        if let Ok(mut cache) = SONG_INFO_CACHE.lock() {
            let map = cache.get_or_insert_with(HashMap::new);
            map.insert(song.path.clone(), song);
        }
        Ok(())
    }

    fn persist_last_played(song: &SongInfo) {
        use crate::settings::LastPlayedTrack;
        let track = LastPlayedTrack {
            path: song.path.clone(),
            title: song.title.clone(),
            artist: song.artist.clone(),
            album: song.album.clone(),
            duration_secs: song.duration_secs,
            album_art_path: song.album_art_path.clone(),
        };
        if let Ok(mut settings) = settings_load() {
            settings.last_played_track = Some(track);
            let _ = settings_save(&settings);
        }
    }

